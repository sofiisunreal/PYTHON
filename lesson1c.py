# numbers
# arithmetic operators 

num1=40
num2=10

#addition
sum=num1+num2
print("addition of", num1,"and", num2,"is", sum)

# subtraction 
difference=num1-num2
print("the difference of", num1,"and", num2,"is", difference)

# multiplication 
product=num1*num2
print("the product of",num1,"and",num2,"is",product)

# division 
quotient=num1/num2
print("the quotient of",num1,"and",num2,"is",quotient)

# modulus
modulus=num1%num2
print("the modulus of",num1,"and",num2,"is", modulus)

# exponent
exponent=num1**num2
print("the exponent of",num1,"and",num2,"is",exponent)